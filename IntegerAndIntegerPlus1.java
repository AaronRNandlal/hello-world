package homework2;

public class IntegerAndIntegerPlus1 {

	//main
	public static void main(String[] args) {
		
		//declares and initializes an integer n with a value of 45
		int n = 45;
		
		//prints a user friendly statement about integer n
		System.out.println("The integer n = " + n);
		
		//prints a user friendly statement about n + 1
		System.out.println("Integer n + 1 = " + ++n);

	}

}
